# Пример использования функции определения напряжения:
размер = input("Введите размер батарейки (A, AA, AAA, AAAA): ")
напряжение = определить_напряжение(размер)

if напряжение:
  print(напряжение)
else:
  print("Неизвестный размер батарейки.")

# Пример использования и демонстрация хеш-функций:
демонстрация_хеш_функций()

