def определить_напряжение(размер_батарейки):
  """
  Определяет примерное напряжение батарейки в зависимости от размера.

  Args:
    размер_батарейки: Строка, представляющая размер батарейки (например, "АА", "ААА").

  Returns:
    Строка с примерным напряжением и дополнительной информацией, или None, если размер батарейки не распознан.
  """

  напряжения = {
      "A": "Обычно 1.5V (щелочная) или 3.0V (литиевая).",
      "AA": "Обычно 1.5V (щелочная) или 1.2V (NiMH).",
      "AAA": "Обычно 1.5V (щелочная) или 1.2V (NiMH).",
      "AAAA": "Обычно 1.5V (щелочная)."
  }

  размер_батарейки = размер_батарейки.upper()  # Приводим к верхнему регистру для регистронезависимости

  if размер_батарейки in напряжения:
    return f"Батарейка размера {размер_батарейки}: {напряжения[размер_батарейки]}"
  else:
    return None #или можно вернуть "Неизвестный размер батарейки"

def hash_function_B(размер_батарейки, table_size=10):
    """Простая хеш-функция, преобразующая размер батарейки в индекс хеш-таблицы."""
    return sum(ord(c) for c in размер_батарейки) % table_size

def hash_function_D(размер_батарейки, table_size=10):
    """Улучшенная хеш-функция, учитывающая позицию символов."""
    hash_value = 0
    for i, c in enumerate(размер_батарейки):
        hash_value = (hash_value * 31 + ord(c)) % table_size  # 31 - простое число для лучшего распределения
    return hash_value

def демонстрация_хеш_функций():
    """Демонстрирует распределение хеш-значений для разных размеров батареек."""

    размеры_батареек = ["A", "AA", "AAA", "AAAA"]

    print("Демонстрация хеш-функций (размер таблицы = 10):")

    print("\nХеш-функция B (простая сумма кодов символов):")
    for размер in размеры_батареек:
        hash_value = hash_function_B(размер)
        print(f"Размер {размер}: хеш-значение = {hash_value}")

    print("\nХеш-функция D (взвешенная сумма кодов символов):")
    for размер in размеры_батареек:
        hash_value = hash_function_D(размер)
        print(f"Размер {размер}: хеш-значение = {hash_value}")

